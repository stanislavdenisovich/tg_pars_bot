    params = ask_chatgpt(answers)
    score = compute_score(
        R=params["reach"],
        I=params["impact"],
        C=params["confidence"],
        E=params["effort"],
        K=params["competition"]
    )

    save_result(user, answers, params, score)

    bot.send_message(chat_id,
        f"‚úÖ –ì–æ—Ç–æ–≤–æ!\n\n"
        f"<b>–û—Ü–µ–Ω–∫–∞ –∏–¥–µ–∏: {score}</b>\n\n"
        f"<pre>{json.dumps(params, indent=2, ensure_ascii=False)}</pre>"
    )

    del USER_STATE[user]
    del ANSWERS[user]

